class ResponseCodes {
  constructor() {
    this.allClear = 0;
    this.noUserFound = 1;
    this.incorrectPassword = 2;
    this.operationFailed = 3;
  }
  allClear(){
    return this.allClear;
  }
  noUserFound(){
    return this.allClear;
  }
  incorrectPassword(){
    return this.allClear;
  }
  operationFailed(){
    return this.allClear;
  }
}
export default ResponseCodes;
